package de.akquinet.timref.rawdatamaster.util

import com.fasterxml.jackson.databind.ObjectMapper
import com.fasterxml.jackson.databind.SerializationFeature
import com.fasterxml.jackson.databind.json.JsonMapper
import com.fasterxml.jackson.dataformat.xml.XmlMapper
import com.fasterxml.jackson.datatype.jsr310.JavaTimeModule
import com.fasterxml.jackson.module.kotlin.registerKotlinModule

private fun ObjectMapper.withDefaultSetup() =
    this.registerKotlinModule()
        .registerModule(JavaTimeModule())
        .enable(SerializationFeature.INDENT_OUTPUT)

internal val defaultJsonMapper = JsonMapper().withDefaultSetup()

internal val defaultXmlMapper = XmlMapper().withDefaultSetup()
